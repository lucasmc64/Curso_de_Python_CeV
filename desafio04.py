algo = input('Digite algo: ')
print('\033[34mO tipo primitivo desse valor é {}\033[m'.format(type(algo)))
print('\033[35mSó tem espaços? {}\033[m'.format(algo.isspace()))
print('\033[34mÉ um número inteiro? {}\033[m'.format(algo.isnumeric()))
print('\033[35mÉ alphanumérico? {}\033[m'.format(algo.isalnum()))
print('\033[34mTem apenas letras? {}\033[m'.format(algo.isalpha()))
print('\033[35mÉ um número decimal? {} \033[m'.format(algo.isdecimal()))
print('\033[34mTodas as letras estão em maiúsculo? {}\033[m'.format(algo.isupper()))
print('\033[35mTodas as letras estão em minúsculo? {}\033[m'.format(algo.islower()))
print('\033[34mEstá capitalizada? {}\033[m'.format(algo.istitle()))
